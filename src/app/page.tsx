const TAB_FILES: Partial<Record<ResultTab, string>> = {
  watch: "watch.md",
  style: "style-bible.md",
  "shot-specs": "shot-specs.md",
  prompt: "codex-prompt.md"
};

export default function Home() {
  // Auth state
  const [user, setUser] = useState<User | null>(null);
  const [billing, setBilling] = useState<BillingSummary | null>(null);
  const [authLoading, setAuthLoading] = useState(true);

  // Form state
  const [url, setUrl] = useState("");
  const [topK, setTopK] = useState(8);
  const [mode, setMode] = useState<"density" | "top-k">("density");
  const [extractionKind, setExtractionKind] = useState<"text" | "full">("text");
  const [candidateIntervalSeconds, setCandidateIntervalSeconds] = useState(8);
  const [maxCandidateFrames, setMaxCandidateFrames] = useState(36);
  const [tuningOpen, setTuningOpen] = useState(false);

  // Run state
  const [phase, setPhase] = useState<Phase>("compose");
  const [progress, setProgress] = useState<ProgressEvent | null>(null);
  const [discoveredTitle, setDiscoveredTitle] = useState<string | null>(null);
  const [elapsedMs, setElapsedMs] = useState(0);
  const [error, setError] = useState("");

  // Result state
  const [result, setResult] = useState<AnalyzeResult | null>(null);
  const [activeTab, setActiveTab] = useState<ResultTab>("style");
  const [viewRaw, setViewRaw] = useState(false);
  const [lightbox, setLightbox] = useState<number | null>(null);
  const [toast, setToast] = useState("");

  // Saved videos — past analyses kept in Postgres for the signed-in user.
  const [saved, setSaved] = useState<SavedVideoSummary[]>([]);
  const [savedLoading, setSavedLoading] = useState(false);

  const abortRef = useRef<AbortController | null>(null);
  const runStartRef = useRef(0);

  const videoId = useMemo(() => parseYouTubeId(url), [url]);
  const canSubmit = url.trim().length > 0 && phase !== "running";

  /* Hydrate the signed-in account and its saved video list. */
  useEffect(() => {
    let alive = true;
    (async () => {
      try {
        const response = await fetch("/api/me", { cache: "no-store" });
        const payload = (await response.json()) as { user: User | null; billing: BillingSummary };
        if (!alive) return;
        setUser(payload.user);
        setBilling(payload.billing);
        setExtractionKind(payload.user ? "full" : "text");
        if (payload.user) await refreshVideos();
      } finally {
        if (alive) setAuthLoading(false);
      }
    })();
    return () => {
      alive = false;
    };
  }, []);

  /* Live elapsed clock while a run is in flight. */
  useEffect(() => {
    if (phase !== "running") return;
    const tick = () => setElapsedMs(Date.now() - runStartRef.current);
    tick();
    const timer = window.setInterval(tick, 250);
    return () => window.clearInterval(timer);
  }, [phase]);

  /* Toast auto-dismiss. */
  useEffect(() => {
    if (!toast) return;
    const timer = window.setTimeout(() => setToast(""), 2200);
    return () => window.clearTimeout(timer);
  }, [toast]);

  /* Lightbox keyboard navigation. */
  useEffect(() => {
    if (lightbox === null || !result) return;
    const onKey = (event: KeyboardEvent) => {
      if (event.key === "Escape") setLightbox(null);
      if (event.key === "ArrowRight")
        setLightbox((index) => (index === null ? null : (index + 1) % result.frames.length));
      if (event.key === "ArrowLeft")
        setLightbox((index) =>
          index === null ? null : (index - 1 + result.frames.length) % result.frames.length
        );
    };
    window.addEventListener("keydown", onKey);
    return () => window.removeEventListener("keydown", onKey);
  }, [lightbox, result]);

  const flashToast = useCallback((message: string) => setToast(message), []);

  async function refreshVideos() {
    setSavedLoading(true);
    try {
      const response = await fetch("/api/videos", { cache: "no-store" });
      if (response.status === 401) {
        setUser(null);
        setExtractionKind("text");
        setSaved([]);
        return;
      }
      const payload = (await response.json()) as { videos?: SavedVideoSummary[] };
      setSaved(payload.videos ?? []);
    } finally {
      setSavedLoading(false);
    }
  }

  function handleAuthenticated(nextUser: User) {
    setUser(nextUser);
    setExtractionKind("full");
    setPhase("compose");
    setResult(null);
    setError("");
    void refreshVideos();
  }

  async function logout() {
    await fetch("/api/auth/logout", { method: "POST" });
    abortRef.current?.abort();
    setUser(null);
    setBilling(null);
    setExtractionKind("text");
    setSaved([]);
    setResult(null);
    setPhase("compose");
    flashToast("Signed out");
  }

  async function submit() {
    if (!canSubmit) return;
    const controller = new AbortController();
    abortRef.current = controller;
    runStartRef.current = Date.now();

    setPhase("running");
    setError("");
    setResult(null);
    setDiscoveredTitle(null);
    setElapsedMs(0);
    setProgress({ stage: "info", label: "Reading metadata", pct: 0, elapsedMs: 0 });

    try {
      const response = await fetch("/api/analyze", {
        method: "POST",
        headers: { "Content-Type": "application/json", Accept: "application/x-ndjson" },
        body: JSON.stringify({
          url,
          topK,
          mode,
          extractionKind,
          candidateIntervalSeconds,
          maxCandidateFrames,
          frameWidth: 768
        }),
        signal: controller.signal
      });

      if (!response.ok || !response.body) {
        const payload = await response.json().catch(() => ({}));
        throw new Error(payload.error || `Request failed (${response.status})`);
      }

      const reader = response.body.getReader();
      const decoder = new TextDecoder();
      let buffer = "";
      let finalResult: AnalyzeResult | null = null;

      const consume = (line: string) => {
        if (!line.trim()) return;
        const event = JSON.parse(line) as
          | ({ type: "progress" } & ProgressEvent)
          | { type: "result"; result: AnalyzeResult }
          | { type: "error"; message: string };
        if (event.type === "progress") {
          setProgress(event);
          if (event.stage === "download" && event.detail) setDiscoveredTitle(event.detail);
        } else if (event.type === "result") {
          finalResult = event.result;
        } else if (event.type === "error") {
          throw new Error(event.message);
        }
      };

      for (;;) {
        const { done, value } = await reader.read();
        if (done) break;
        buffer += decoder.decode(value, { stream: true });
        const lines = buffer.split("\n");
        buffer = lines.pop() ?? "";
        for (const line of lines) consume(line);
      }
      if (buffer.trim()) consume(buffer);

      if (!finalResult) throw new Error("The analysis stream ended without a result.");
      const completedResult = finalResult as AnalyzeResult;
      setResult(completedResult);
      setActiveTab(completedResult.extractionKind === "text" ? "watch" : "style");
      setViewRaw(false);
      setPhase("done");
      await refreshBilling();
      await refreshVideos();
    } catch (caught) {
      if (controller.signal.aborted) {
        setPhase("compose");
        return;
      }
      setError(caught instanceof Error ? caught.message : "Analysis failed");
      setPhase("compose");
    } finally {
      abortRef.current = null;
    }
  }

  function cancelRun() {
    abortRef.current?.abort();
    setPhase("compose");
  }

  function resetToCompose() {
    setPhase("compose");
    setResult(null);
    setProgress(null);
    setError("");
  }

  /* Re-open a saved video straight into its result view. */
  async function openSavedVideo(video: SavedVideoSummary) {
    abortRef.current?.abort();
    const response = await fetch(`/api/videos/${video.id}`, { cache: "no-store" });
    if (!response.ok) {
      flashToast(response.status === 404 ? "Video not found" : "Could not open video");
      if (response.status === 401) setUser(null);
      return;
    }
    const payload = (await response.json()) as { video: { result: AnalyzeResult; sourceUrl: string } };
    setUrl(payload.video.sourceUrl);
    setResult(payload.video.result);
    setActiveTab("style");
    setViewRaw(false);
    setLightbox(null);
    setError("");
    setProgress(null);
    setPhase("done");
    if (typeof window !== "undefined") window.scrollTo({ top: 0, behavior: "smooth" });
  }

  async function removeSavedVideo(id: string) {
    const response = await fetch(`/api/videos/${id}`, { method: "DELETE" });
    if (response.status === 401) {
      setUser(null);
      return;
    }
    setSaved((videos) => videos.filter((video) => video.id !== id));
    flashToast("Video deleted");
  }

  async function refreshBilling() {
    const response = await fetch("/api/me", { cache: "no-store" });
    const payload = (await response.json()) as { user: User | null; billing: BillingSummary };
    setUser(payload.user);
    setBilling(payload.billing);
  }

  async function startCheckout(action: "credits" | "recurring" | "setup", packId?: "starter" | "studio") {
    const response = await fetch("/api/billing/checkout", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ action, packId })
    });
    const payload = (await response.json()) as { url?: string; error?: string };
    if (!response.ok || !payload.url) {
      flashToast(payload.error || "Billing could not start");
      return;
    }
    window.location.href = payload.url;
  }

  async function openBillingPortal() {
    const response = await fetch("/api/billing/portal", { method: "POST" });
    const payload = (await response.json()) as { url?: string; error?: string };
    if (!response.ok || !payload.url) {
      flashToast(payload.error || "Billing portal unavailable");
      return;
    }
    window.location.href = payload.url;
  }

  async function updateBillingSettings(settings: {
    autoRefillEnabled?: boolean;
    recurringEnabled?: boolean;
  }) {
    const response = await fetch("/api/billing/settings", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(settings)
    });
    if (!response.ok) {
      flashToast("Billing settings were not updated");
      return;
    }
    await refreshBilling();
  }

  /* Text backing the active document tab. */
  const activeText = useMemo(() => {
    if (!result) return "";
    if (activeTab === "watch") return result.markdown;
    if (activeTab === "style") return result.cinematic.styleMarkdown;
    if (activeTab === "shot-specs") return result.cinematic.shotSpecMarkdown;
    if (activeTab === "prompt") return result.cinematic.promptMarkdown;
    if (activeTab === "warnings") return warningsToText(result.cinematic.slopWarnings);
    return "";
  }, [activeTab, result]);

  const renderedDoc = useMemo(
    () => (activeText ? markdownToHtml(activeText) : ""),
    [activeText]
  );

  async function copyActiveText() {
    if (!activeText) return;
    try {
      await navigator.clipboard.writeText(activeText);
      flashToast(`${TAB_LABELS[activeTab]} copied to clipboard`);
    } catch {
      flashToast("Clipboard unavailable in this browser");
    }
  }

  /* ---------------------------------------------------------------- *
   * Render                                                            *
   * ---------------------------------------------------------------- */

  const stageIndex = progress ? STAGE_PLAN.findIndex((entry) => entry.stage === progress.stage) : -1;
  const isDone = progress?.stage === "done";
  const pctLabel = Math.round((progress?.pct ?? 0) * 100);
  const runningTitle = discoveredTitle || "Reading your video";

  return (
    <main className="app">
      <div className="aurora" aria-hidden="true" />

      <SiteHeader />

      <section className="stage">
        {authLoading ? (
          <div className="auth-loading" role="status">
            <span className="auth-loading-spin" aria-hidden="true" />
            <span>Loading your account</span>
          </div>
        ) : phase === "compose" ? (
          <ComposeView
            user={user}
            billing={billing}
            onLogout={logout}
            onAuthenticated={handleAuthenticated}
            url={url}
            setUrl={setUrl}
            videoId={videoId}
            extractionKind={extractionKind}
            setExtractionKind={setExtractionKind}
            topK={topK}
            setTopK={setTopK}
            mode={mode}
            setMode={setMode}
            candidateIntervalSeconds={candidateIntervalSeconds}
            setCandidateIntervalSeconds={setCandidateIntervalSeconds}
            maxCandidateFrames={maxCandidateFrames}
            setMaxCandidateFrames={setMaxCandidateFrames}
            tuningOpen={tuningOpen}
            setTuningOpen={setTuningOpen}
            canSubmit={canSubmit}
            error={error}
            onSubmit={submit}
            saved={saved}
            savedLoading={savedLoading}
            onOpenSaved={openSavedVideo}
            onDeleteSaved={removeSavedVideo}
            onCheckout={startCheckout}
            onPortal={openBillingPortal}
            onBillingSettings={updateBillingSettings}
          />
        ) : null}

        {phase === "running" ? (
          <RunningView
            videoId={videoId}
            title={runningTitle}
            progress={progress}
            stageIndex={stageIndex}
            isDone={isDone}
            pctLabel={pctLabel}
            elapsedMs={elapsedMs}
            onCancel={cancelRun}
          />
        ) : null}

        {phase === "done" && result ? (
          <ResultView
            result={result}
            videoId={videoId}
            activeTab={activeTab}
            setActiveTab={(tab) => {
              setActiveTab(tab);
              setViewRaw(false);
            }}
            viewRaw={viewRaw}
            setViewRaw={setViewRaw}
            renderedDoc={renderedDoc}
            activeText={activeText}
            onCopy={copyActiveText}
            onReset={resetToCompose}
            openLightbox={setLightbox}
            flashToast={flashToast}
          />
        ) : null}
      </section>

      <SiteFooter />

      {lightbox !== null && result ? (
        <Lightbox
          frames={result.frames}
          index={lightbox}
          onClose={() => setLightbox(null)}
          onNavigate={(next) => setLightbox(next)}
          flashToast={flashToast}
        />
      ) : null}

      <div className={`toast ${toast ? "toast--show" : ""}`} role="status" aria-live="polite">
        {toast}
      </div>
    </main>
  );
}

