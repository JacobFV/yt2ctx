function Lightbox(props: {
  frames: Frame[];
  index: number;
  onClose: () => void;
  onNavigate: (next: number) => void;
  flashToast: (message: string) => void;
}) {
  const frame = props.frames[props.index];
  if (!frame) return null;
  const go = (delta: number) =>
    props.onNavigate((props.index + delta + props.frames.length) % props.frames.length);

  return (
    <div className="lightbox" role="dialog" aria-modal="true" onClick={props.onClose}>
      <button className="lightbox-close" type="button" onClick={props.onClose} aria-label="Close">
        ✕
      </button>
      <button
        className="lightbox-nav lightbox-nav--prev"
        type="button"
        onClick={(event) => {
          event.stopPropagation();
          go(-1);
        }}
        aria-label="Previous frame"
      >
        ‹
      </button>
      <div className="lightbox-inner" onClick={(event) => event.stopPropagation()}>
        <img className="lightbox-img" src={frame.imageUrl} alt={frame.description} />
        <div className="lightbox-side">
          <div className="lightbox-head">
            <span className="lightbox-time">{formatTimestamp(frame.timestamp)}</span>
            <span className="lightbox-index">
              {props.index + 1} / {props.frames.length}
            </span>
          </div>
          <p className="lightbox-desc">{frame.description}</p>
          <div className="tag-row">
            {frame.labels.map((label) => (
              <span className="tag" key={label}>
                {label}
              </span>
            ))}
          </div>
          {frame.transcriptContext ? (
            <div className="lightbox-transcript">
              <span className="lightbox-field-label">Nearby transcript</span>
              <p>{frame.transcriptContext}</p>
            </div>
          ) : null}
          <div className="lightbox-score">
            <span className="lightbox-field-label">Salience score</span>
            <span className="score-meter">
              <span
                className="score-meter-fill"
                style={{ width: `${Math.min(100, Math.round(frame.score * 100))}%` }}
              />
            </span>
            <span>{frame.score.toFixed(3)}</span>
          </div>
          <button
            type="button"
            className="btn btn-primary btn-sm"
            onClick={() => {
              downloadBlob(frame.imageDownloadUrl, frame.fileName);
              props.flashToast("Frame downloading");
            }}
          >
            Download frame
          </button>
        </div>
      </div>
      <button
        className="lightbox-nav lightbox-nav--next"
        type="button"
        onClick={(event) => {
          event.stopPropagation();
          go(1);
        }}
        aria-label="Next frame"
      >
        ›
      </button>
    </div>
  );
}
