function WarningsList(props: { warnings: SlopWarning[]; onCopy: () => void }) {
  if (props.warnings.length === 0) {
    return <p className="empty-note">No slop warnings were generated for this reference.</p>;
  }
  return (
    <div className="warn-wrap">
      <div className="doc-toolbar">
        <p className="warn-intro">Validator-ready rules — reject generated shots that trip these.</p>
        <button type="button" className="btn btn-ghost btn-sm" onClick={props.onCopy}>
          Copy all
        </button>
      </div>
      <div className="warn-list">
        {props.warnings.map((warning) => (
          <article className="warn-card" key={warning.rule}>
            <h3 className="warn-rule">{warning.rule}</h3>
            <p className="warn-why">{warning.whyItBreaksTaste}</p>
            <dl className="warn-dl">
              <dt>Reject if</dt>
              <dd>{warning.rejectIf}</dd>
              <dt>Prefer</dt>
              <dd>{warning.preferredMove}</dd>
            </dl>
          </article>
        ))}
      </div>
    </div>
  );
}

